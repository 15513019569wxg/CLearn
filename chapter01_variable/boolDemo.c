#include <stdio.h>

//宏定义
#define BOOL int
#define TURE 1

int main() {
    int isPass = -1;
    //可以使用宏定义来完成(这里大家先看下，体会一下，后面我们会详解宏定义)
    //定义一个布尔变量
    BOOL isOk = TURE; // 等价 int isOK = 0

    if (isPass) { // 0表示假， 非0表示真
        printf("通过考试");
    }

    if (isOk) {
        printf("ok");
    }

}